    def smma(self, last, data, p):
        """
        计算smma
        :return:
        """
        # 这里可以调整，从第1日算 还是第N日算 smma
        alpha = 1 / p
        return round(data * alpha + last * (1 - alpha), self.accuracy)

    def cal_index(self, data):
        # 初始化第一个数字
        high = float(data.get('H'))
        low = float(data.get('L'))
        close = float(data.get('C'))
        self.low.append(low)
        self.high.append(high)
        if len(self.low) >= self.n + 1:
            self.low.pop(0)
            self.high.pop(0)
        elif len(self.low) == self.n:
            pass
        else:
            return
        if max(self.high) - min(self.low) == 0:
            rsv = 50
        else:
            rsv = (close - min(self.low)) / (max(self.high) - min(self.low)) * 100
        if self.k_buffer[-1]:
            self.K = self.smma(self.k_buffer[-1], rsv, self.p1)
        else:
            self.K = round(rsv, self.accuracy)
        self.k_buffer.append(self.K)
        if self.d_buffer[-1]:
            self.D = self.smma(self.d_buffer[-1], self.K, self.p2)
        else:
            self.D = round(self.K, self.accuracy)
        self.d_buffer.append(self.D)
        self.J = self.K * 3 - 2 * self.D
        self.j_buffer.append(self.J)
        if len(self.k_buffer) > 20:
            self.k_buffer.pop(0)
            self.d_buffer.pop(0)
            self.j_buffer.pop(0)

    def get_signal(self):
        if self.k_buffer[-2] <= self.d_buffer[-2] and self.k_buffer[-2] <= 20 and self.k_buffer[-1] >= self.d_buffer[-1]:
            open_side = 'buy'
            return open_side
        elif self.k_buffer[-2] >= self.d_buffer[-2] and self.k_buffer[-2] >= 80 and self.k_buffer[-1] <= self.d_buffer[-1]:
            open_side = 'sell'
            return open_side

